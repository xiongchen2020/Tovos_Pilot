package com.tovos.uav.sample.databean;

public class RtkInfo {

    private String ip;
    private String user;
    private String pwd;
    private String gzd;
    private String post;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPwd() {
        return pwd;
    }

    public void setPwd(String pwd) {
        this.pwd = pwd;
    }

    public String getGzd() {
        return gzd;
    }

    public void setGzd(String gzd) {
        this.gzd = gzd;
    }

    public String getPost() {
        return post;
    }

    public void setPost(String post) {
        this.post = post;
    }
}
